// 获取随机签文
function getRandomSign() {
  const randomIndex = Math.floor(Math.random() * SIGNS.length)
  return SIGNS[randomIndex]
}

// 根据签号获取签文
function getSignByNumber(number) {
  return SIGNS.find(sign => sign.number === number)
}

